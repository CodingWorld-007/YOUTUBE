#Sum Operation
x <- 1L 
y <- 2  
x <- x + y
print(x)
#typeof function
print(typeof(x))

#Difference Operation
a <- 3L
b <- 4L
a <- a - b
print(a)
print(typeof(a))

#Product Operation 
a <- 3L
b <- 4L
a <- a * b
print(a)
print(typeof(a))

#Reminder Operation
r <- 10L %% 2L
print(r)
print(typeof(r))

